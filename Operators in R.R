#Operators in R
#Arithematic Operators  + - * / %% %/% ^
#Relational Operators < > <= >= != == 
#Logical Operators & | ! && ||
#Assignment Operators = <- -> <<- ->>

a<-7.5
b<-2
print(a+b) #Addition
print(a-b) #Substraction
print(a*b) #Multiplication
print(a/b) #Division
print(a%%b) #Remainder
print(a%/%b) #Quotient
print(a^b) #Power of

#Vectors Operations
c1<-c(2,3,4)
c2<-c(5,6,7)
print(c1+c2) #Addition
print(c1-c2) #Substraction
print(c1*c2) #Multiplication
print(c1/c2) #Division
print(c1%%c2)#Remainder
print(c1%/%c2) #Quotient
print(c1^c2) #Power of

#Relational Operators
print(a<b) #Addition
print(a>b) #Substraction
print(a<=b) #Multiplication
print(a>=b) #Division
print(a==b) #Remainder
print(a!=b) #Quotient

#Logical Operators
d<-c(2.3,TRUE,2+5i)
e<-c(3.4,FALSE,1-6i)
print(d&e) #Addition
print(d|e) #Substraction
print(! e) #Multiplication
print(d||e) #Division
print(d&&e) #Remainder

#Assignment Operators
print(a<-b) #Addition
print(a->b) #Substraction
print(a=b) #Multiplication
print(a<<-b) #Division
print(a->>b) #Remainder

