# Write a program to create a list to add/remove the elements from and to list?
# creating a list
my_list <- list(Name = "Kush" , age=24, subject = "R Programming")
print("printing the list")
print(my_list)

# adding new element to the list
my_list$Name2 <- "KUSH"
